package Adapters;

public class listItem {
    private final String Head;
    private String head;
    private String Desc;

    public listItem(String head, String desc) {
        Head = head;
        Desc = desc;
    }

    public static String getHead() {
        String Head;
        return  null;
    }



    public String getDesc() {
        return Desc;
    }

    public void setDesc(String desc) {
        Desc = desc;
    }
}
